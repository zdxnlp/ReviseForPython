"""
解题思路
这是一道典型的贪心算法问题，核心目标是用最少的优化器覆盖所有光伏板，贪心策略的核心是：对于未被覆盖的光伏板，优先将优化器放在其右侧的空位，这样可以让一个优化器覆盖尽可能多的后续光伏板，从而最小化优化器数量。
核心逻辑
覆盖规则：优化器只能放在空位（0），一个优化器放在位置pos，可以覆盖其左侧pos-1和右侧pos+1的光伏板（1）。
贪心策略：从左到右遍历数组，遇到未被覆盖的光伏板时，优先在其右侧空位安装优化器，这样可以同时覆盖当前光伏板和右侧相邻的光伏板（如果存在），最大化单个优化器的覆盖范围。
异常处理：如果当前光伏板的左右都没有可安装优化器的空位，说明无法覆盖所有光伏板，直接返回 - 1。
遍历规则
遇到空位（0）：直接跳过，遍历下一个位置。
遇到光伏板（1）：
优先检查右侧（i+1）是否是合法空位，若是则安装优化器，计数 + 1，然后直接跳到i+3的位置（因为i已被覆盖，i+1是优化器，i+2的光伏板也会被当前优化器覆盖，无需处理）。
若右侧无法安装，检查左侧（i-1）是否是合法空位，若是则安装优化器，计数 + 1，然后跳到i+1的位置（仅覆盖了当前光伏板，后续位置需要继续处理）。
若左右都无法安装，直接返回 - 1。
"""

def main():
    import sys
    # 一次性读取所有输入，适配大数据量，避免IO超时
    n = int(input())
    pvs = list(map(int, input().split()))

    res = 0
    i = 0
    while i < n:
        if pvs[i] == 0:
            i += 1
        else:
            # 优先在光伏板右侧安装优化器，最大化覆盖范围
            if i + 1 < n and pvs[i + 1] == 0:
                res += 1
                i += 3  # 跳过已覆盖的i、优化器位置i+1、被覆盖的i+2
            else:
                # 右侧无法安装，尝试左侧
                if i - 1 >= 0 and pvs[i - 1] == 0:
                    res += 1
                    i += 1  # 仅覆盖当前i，继续处理下一个位置
                else:
                    # 无可用空位，无法覆盖所有光伏板
                    print(-1)
                    return
    print(res)


if __name__ == "__main__":
    main()