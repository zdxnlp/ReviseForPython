"""
要求连续的最长公共子串
"""

"""
核心方法：动态规划（DP）
状态定义定义二维数组dp[i][j]，表示以第一个字符串的第 i 个字符结尾、第二个字符串的第 j 个字符结尾的最长连续公共子串的长度。（注：字符串使用 0 下标，dp 数组使用 1 下标，避免处理 i=0/j=0 的边界异常）
状态转移方程
当s1[i-1] == s2[j-1]（当前两个字符相等）：dp[i][j] = dp[i-1][j-1] + 1。
含义：当前字符可以接在前面的公共子串后，连续长度 + 1。
当s1[i-1] != s2[j-1]（当前两个字符不相等）：dp[i][j] = 0。
含义：连续子串中断，以这两个字符结尾的公共子串长度为 0。
边界条件dp[0][j] = 0、dp[i][0] = 0：空字符串与任何字符串的公共子串长度为 0。
结果统计遍历 dp 数组的过程中，维护一个全局最大值max_len，记录所有dp[i][j]的最大值，最终返回该值即可。
复杂度分析
时间复杂度：O (n*m)，n、m 为两个字符串的长度，本题 n、m≤1000，总运算量 1e6，完全满足性能要求。
空间复杂度：O (n*m)，可优化至 O (min (n,m))，基础版本使用二维数组更直观易理解。
"""
def main():
    import sys
    # 读取输入，过滤空行，处理OJ输入的边界情况
    input_lines = [line.strip() for line in sys.stdin if line.strip()]
    s1 = input_lines[0]
    s2 = input_lines[1]

    n = len(s1)
    m = len(s2)

    # 初始化dp数组，(n+1)行 (m+1)列，初始值全0
    dp = [[0] * (m + 1) for _ in range(n + 1)]
    max_len = 0  # 记录最长公共子串长度

    for i in range(1, n + 1):
        for j in range(1, m + 1):
            if s1[i - 1] == s2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
                # 更新全局最大值
                if dp[i][j] > max_len:
                    max_len = dp[i][j]
            # 字符不相等时，dp[i][j]保持初始值0，无需额外处理

    print(max_len)


if __name__ == "__main__":
    main()